async def _check_admin(message: types.Message) -> bool:
    """
    Возвращает True/False.
    По возможности использует общую админ-проверку проекта.
    """
    # 1) Основной ожидаемый путь (как в main.py: handlers.admin.*)
    try:
        from handlers.admin.admin_helpers import check_admin_async  # type: ignore
        return await check_admin_async(message)
    except Exception as e:
        logger.debug(f"Failed to import from handlers.admin.admin_helpers: {e}")

    # 2) Фолбэк для альтернативной структуры (если admin лежит рядом)
    try:
        from admin.admin_helpers import check_admin_async  # type: ignore
        return await check_admin_async(message)
    except Exception as e:
        logger.debug(f"Failed to import from admin.admin_helpers: {e}")

    # 3) Последний фолбэк: пробуем проверку по списку ADMIN_IDS
    try:
        try:
            from handlers.admin.admin_constants import ADMIN_IDS  # type: ignore
        except Exception:
            try:
                from admin.admin_constants import ADMIN_IDS  # type: ignore
            except Exception as e:
                logger.debug(f"Failed to import ADMIN_IDS: {e}")
                ADMIN_IDS = []

        if message.from_user and message.from_user.id in set(ADMIN_IDS):
            return True
    except Exception as e:
        logger.debug(f"Error checking ADMIN_IDS: {e}")

    # Если не смогли проверить — лучше запретить (безопаснее)
    await message.reply("❌ Команда доступна только администраторам.")
    return False



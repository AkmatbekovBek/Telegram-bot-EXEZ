def register_ai_handlers(dp: Dispatcher):
    """Регистрация обработчиков AI с НИЗКИМ приоритетом"""

    # 1. Команды администратора
    dp.register_message_handler(ai_on_command, commands=['aion'])
    dp.register_message_handler(ai_off_command, commands=['aioff'])
    dp.register_message_handler(ai_status_handler, commands=['ai_status'])

    # 2. Стили ИИ
    dp.register_message_handler(ai_style_command, commands=['aistyle'])
    dp.register_message_handler(ai_style_status, commands=['aistyle_status'])

    # 3. AI-чат в группах - ВАЖНО: этот хендлер должен быть ПОСЛЕДНИМ
    # чтобы не перехватывать сообщения, предназначенные для других модулей
    dp.register_message_handler(
        ai_chat_handler,
        lambda m: m.chat.type in ['group', 'supergroup']
                  and m.text
                  and not m.text.startswith('/'),
        state="*",
        content_types=types.ContentType.TEXT
    )

    logger.info(f"✅ AI handlers registered with {len(ai_bot.all_responses)} response variations")
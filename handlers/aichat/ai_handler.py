    async def get_response(self, message: types.Message) -> str:
        """Основной метод получения ответа от ИИ"""
        if not self.is_enabled(message.chat.id):
            logger.debug(f"ИИ отключен в чате {message.chat.id}")
            return ""

        text = message.text or ""

        # Проверяем, не нужно ли игнорировать сообщение
        if self.should_ignore(text):
            logger.debug(f"ИИ игнорирует сообщение в чате {message.chat.id}: '{text[:50]}...'")
            return ""

        # ✅ БЫСТРЫЕ ОСОЗНАННЫЕ ОТВЕТЫ НА ТРИГГЕРНЫЕ СЛОВА
        text_lower = text.lower()


async def ai_chat_handler(message: types.Message):
    """
    Обработчик сообщений для ИИ-чата.
    Важно: если ИИ НЕ отвечает — кидаем SkipHandler,
    чтобы сообщение могли обработать другие модули/игры/команды.
    """
    try:
        # Проверяем, не является ли сообщение командой для другого бота
        text = message.text or ""
        text_lower = text.lower()

        # Более строгая проверка на команды для других ботов
        other_bot_patterns = [
            r'^бот\s+ищи',
            r'^бот\s+стоп',
            r'^бот\s+поиск',
            r'^бот,?\s*ищи',
            r'^бот,?\s*стоп',
            r'^бот,?\s*поиск',
            r'^ищи\b',
            r'^стоп\b',
            r'^поиск\b',
        ]

        for pattern in other_bot_patterns:
            if re.match(pattern, text_lower, re.IGNORECASE):
                logger.debug(f"Пропускаем команду для другого бота: {text}")
                raise SkipHandler

        # Получаем ответ от ИИ
        response = await ai_bot.get_response(message)
        if not response:
            raise SkipHandler

        # Отправляем ответ
        await message.answer(response)

    except SkipHandler:
        raise
    except Exception as e:
        logger.error(f"AI handler error: {e}")
        raise SkipHandler



    def should_ignore(self, text: str) -> bool:
        """Проверяем, нужно ли игнорировать сообщение"""
        text = (text or "").strip()
        if not text:
            return True

        # слишком короткие сообщения не трогаем
        if len(text) < 2:
            return True

        # проверяем на команды для других ботов и игр
        for rx in self._ignore_regex:
            if rx.search(text):
                logger.debug(f"ИИ игнорирует сообщение: '{text[:50]}...' - совпало с regex: {rx.pattern[:30]}")
                return True

        return False

    def should_respond(self, message: types.Message) -> bool:
        """Определяем, нужно ли отвечать на сообщение"""
        chat_id = message.chat.id
        now = time.time()

        # 🔥 ЕСЛИ ОТВЕТ НА СООБЩЕНИЕ БОТА — ОТВЕЧАЕМ ВСЕГДА
        if (message.reply_to_message and message.reply_to_message.from_user
                and message.reply_to_message.from_user.is_bot):
            return True

        # кулдаун только для случайных ответов
        last = self.last_response_time.get(chat_id, 0)
        if now - last < self.response_cooldown:
            return False

        text = (message.text or "").lower()

        # упоминание бота — почти всегда
        is_mentioned = any(x in text for x in [
            "бот", "exez", "ии", "ai", "искуственный интеллект",
            "нейросеть", "чатгпт", "гпт", "@exezbot"
        ])

        # ✅ ИСКЛЮЧАЕМ ЛОЖНЫЕ СРАБАТЫВАНИЯ
        bot_command_patterns = ["бот ищи", "бот стоп", "бот, ищи", "бот, стоп",
                                "бот поиск", "ботстарт", "ботстоп"]
        if any(pattern in text for pattern in bot_command_patterns):
            return False

        if is_mentioned:
            return True

        # случайные ответы — редко (10% шанс)
        return random.random() < 0.10

    def update_context(self, chat_id: int, user_message: str, bot_response: str) -> None:
        """Обновляем контекст разговора"""
        if chat_id not in self.context_history:
            self.context_history[chat_id] = []

        # Ограничиваем историю
        self.context_history[chat_id].append({
            "user": user_message[:200],  # Ограничиваем длину
            "bot": bot_response[:200]
        })

        if len(self.context_history[chat_id]) > self.max_context_messages:
            self.context_history[chat_id] = self.context_history[chat_id][-self.max_context_messages:]

    def get_context_prompt(self, chat_id: int) -> str:
        """Формируем промпт с контекстом"""
        if chat_id not in self.context_history or not self.context_history[chat_id]:
            return ""

        ctx = []
        for x in self.context_history[chat_id][-8:]:  # Берем последние 8 сообщений
            ctx.append(f"Пользователь: {x['user']}")
            ctx.append(f"Ты: {x['bot']}")

        return "\n".join(ctx) + "\n"

